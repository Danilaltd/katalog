function makeButton(i){
    var button = document.createElement('button');
    buyA = document.getElementById('a-'+parseInt(i+1));
    button.innerHTML = 'Купить в 1 клик';
    button.style.coolor='white';
    button.style.backgroundColor='#9c0e21';
    button.addEventListener("mouseover", changeDefOver);
    button.addEventListener("mouseout", changeDefOut);
    function changeDefOver(event){
        button.style.backgroundColor='#b41f33';
    }
    function changeDefOut(event){
        button.style.backgroundColor='#9c0e21';
    }
    button.style.color='white';
    buyA.append(button);
}